def plus(a, b):
    return (int(a) + int(b))


def minus(a, b):
    return (int(a) - int(b))


def times(a, b):
    return (int(a) * int(b))


def division(a, b):
    return (int(a) / int(b))


def negation(a):
    return (int(a) * -1)


def power(a, b):
    return (int(a)**int(b))


print(plus(1, 2))
print(minus(4, 3))
print(times(5, 6))
print(division(8, 4))
print(negation(7))
print(power(2, 10))
